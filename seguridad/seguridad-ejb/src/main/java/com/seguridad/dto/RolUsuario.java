package com.seguridad.dto;

import java.io.Serializable;

public class RolUsuario implements Serializable {

    private static final long serialVersionUID = 1L;
    private Integer rouId;
    private Rol secRolRoles;
    private Usuario secUsrUsuarios;

    public RolUsuario() {
    }

    public RolUsuario(Rol secRolRoles, Usuario secUsrUsuarios) {
        this.secRolRoles = secRolRoles;
        this.secUsrUsuarios = secUsrUsuarios;
    }
    

    public RolUsuario(Integer rouId) {
        this.rouId = rouId;
    }

    public Integer getRouId() {
        return rouId;
    }

    public void setRouId(Integer rouId) {
        this.rouId = rouId;
    }

    public Rol getSecRolRoles() {
        return secRolRoles;
    }

    public void setSecRolRoles(Rol secRolRoles) {
        this.secRolRoles = secRolRoles;
    }

    public Usuario getSecUsrUsuarios() {
        return secUsrUsuarios;
    }

    public void setSecUsrUsuarios(Usuario secUsrUsuarios) {
        this.secUsrUsuarios = secUsrUsuarios;
    }

    @Override
    public String toString() {
        return "com.seguridad.modelo.RolUsuario[ rouId=" + rouId + " ]";
    }

}
